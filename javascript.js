let add = function (n1, n2) {
    let total = n1 += n2;
    return total;
} 

let subtract = function (n1, n2) {
    let total = n1 -= n2;
    return total;
}

let multiply = function (n1, n2) {
    let total = n1 *= n2;
    return total;
}

let divide = function (n1, n2) {
    let total = n1 /= n2;
    return total;
}